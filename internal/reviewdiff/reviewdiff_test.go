func TestMarkGeneratedAndLarge(t *testing.T) {
	opts := RenderOptions{
		MaxTotalDiffBytes:        10000,
		MaxFileDiffBytes:         20,
		MaxIncludedFiles:         10,
		MaxOmittedSummaryEntries: 10,
	}

	tests := []struct {
		name          string
		file          ChangedFile
		wantGenerated bool
		wantLarge     bool
	}{
		{name: "generated path", file: ChangedFile{Path: "dist/app.js", Patch: "small"}, wantGenerated: true},
		{name: "generated header", file: ChangedFile{Path: "api.pb.go", Patch: "// Code generated by protoc. DO NOT EDIT.\n"}, wantGenerated: true, wantLarge: true},
		{name: "large lockfile", file: ChangedFile{Path: "yarn.lock", Patch: strings.Repeat("x", LargeLockfileDiffBytes)}, wantLarge: true},
		{name: "over per-file limit", file: ChangedFile{Path: "src/app.go", Patch: strings.Repeat("x", 21)}, wantLarge: true},
		{name: "small source", file: ChangedFile{Path: "src/app.go", Patch: "small"}},
	}

	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			got := MarkGeneratedAndLarge(tt.file, opts)
			assert.Equal(t, tt.wantGenerated, got.Generated)
			assert.Equal(t, tt.wantLarge, got.Large)
		})
	}
}

func TestRenderForReview_SummarizesGeneratedBinaryLargeLockfileAndModeOnly(t *testing.T) {
	largeLockPatch := strings.Repeat("x", LargeLockfileDiffBytes)
	diff := DiffSet{
		Source:   "github",
		BaseSHA:  "base",
		HeadSHA:  "head",
		Warnings: []string{"source warning"},
		Files: []ChangedFile{
			{Path: "dist/app.js", Status: ChangeModified, Patch: "@@ -1 +1 @@\n-old\n+new\n"},
			{Path: "image.png", Status: ChangeAdded, Binary: true},
			{Path: "package-lock.json", Status: ChangeModified, Patch: largeLockPatch},
			{Path: "script.sh", Status: ChangeModified, PreviousMode: "100644", CurrentMode: "100755"},
		},
	}

	result := RenderForReview(diff, RenderOptions{
		MaxTotalDiffBytes:        LargeLockfileDiffBytes + 1000,
		MaxFileDiffBytes:         LargeLockfileDiffBytes + 100,
		MaxIncludedFiles:         10,
		MaxOmittedSummaryEntries: 10,
	})

	require.True(t, result.WasLimited)
	require.Len(t, result.OmittedFiles, 4)
	assert.Contains(t, result.Text, "- Warning: source warning")
	assert.Contains(t, result.Text, "dist/app.js (modified, +0/-0, reason: generated file)")
	assert.Contains(t, result.Text, "image.png (added, +0/-0, reason: binary file)")
	assert.Contains(t, result.Text, "package-lock.json (modified, +0/-0, reason: large lockfile diff)")
	assert.Contains(t, result.Text, "script.sh (modified, +0/-0, mode: 100644 -> 100755, reason: mode-only change)")
	assert.Contains(t, strings.Join(result.Warnings, "\n"), "omitted")
	assert.Contains(t, strings.Join(result.Warnings, "\n"), "generated")
	assert.Contains(t, strings.Join(result.Warnings, "\n"), "binary")
}

func TestRenderForReview_TruncatesPerFileDiff(t *testing.T) {
	result := RenderForReview(DiffSet{
		Source:  "github",
		BaseSHA: "base",
		HeadSHA: "head",
		Files: []ChangedFile{{
			Path:   "large.go",
			Status: ChangeModified,
			Patch:  "@@ -1 +1 @@\n" + strings.Repeat("+x\n", 20),
		}},
	}, RenderOptions{
		MaxTotalDiffBytes:        1000,
		MaxFileDiffBytes:         25,
		MaxIncludedFiles:         10,
		MaxOmittedSummaryEntries: 10,
	})

	require.True(t, result.WasLimited)
	require.Len(t, result.IncludedFiles, 1)
	require.Len(t, result.TruncatedFiles, 1)
	assert.Empty(t, result.OmittedFiles)
	assert.Contains(t, result.Text, "- Warning: truncated, reason: per-file diff byte limit reached")
	assert.Contains(t, result.Text, "[diff truncated]")
	assert.Contains(t, strings.Join(result.Warnings, "\n"), "truncated")
}

func TestRenderForReview_OmitsWhenTotalLimitWouldBeExceeded(t *testing.T) {
	result := RenderForReview(DiffSet{
		Source:  "github",
		BaseSHA: "base",
		HeadSHA: "head",
		Files: []ChangedFile{
			{Path: "one.go", Status: ChangeModified, Patch: strings.Repeat("a", 20)},
			{Path: "two.go", Status: ChangeModified, Patch: strings.Repeat("b", 20)},
		},
	}, RenderOptions{
		MaxTotalDiffBytes:        30,
		MaxFileDiffBytes:         100,
		MaxIncludedFiles:         10,
		MaxOmittedSummaryEntries: 10,
	})

	require.True(t, result.WasLimited)
	require.Len(t, result.IncludedFiles, 1)
	require.Len(t, result.OmittedFiles, 1)
	assert.Equal(t, "two.go", result.OmittedFiles[0].Path)
	assert.Equal(t, "total diff byte limit reached", result.OmittedFiles[0].OmitReason)
	assert.Contains(t, result.Text, "two.go (modified, +0/-0, reason: total diff byte limit reached)")
}

