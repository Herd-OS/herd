func TestRenderForReview_CapsOmittedSummaryEntries(t *testing.T) {
	files := make([]ChangedFile, 0, 4)
	for i := range 4 {
		files = append(files, ChangedFile{
			Path:   fmt.Sprintf("dist/%d.js", i),
			Status: ChangeModified,
			Patch:  "generated",
		})
	}

	result := RenderForReview(DiffSet{
		Source:  "github",
		BaseSHA: "base",
		HeadSHA: "head",
		Files:   files,
	}, RenderOptions{
		MaxTotalDiffBytes:        100,
		MaxFileDiffBytes:         100,
		MaxIncludedFiles:         10,
		MaxOmittedSummaryEntries: 2,
	})

	require.Len(t, result.OmittedFiles, 4)
	assert.Contains(t, result.Text, "dist/0.js")
	assert.Contains(t, result.Text, "dist/1.js")
	assert.NotContains(t, result.Text, "dist/2.js (")
	assert.NotContains(t, result.Text, "dist/3.js (")
	assert.Contains(t, result.Text, "- ... 2 additional omitted files not shown")
}

